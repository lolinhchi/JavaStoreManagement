    private void LoginBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_LoginBtnMouseClicked
        if(userName.getText().isEmpty() || passWord.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter Username or Password");
        }
        else{
            String query = "select * from User1.USERTBL where UNAME='"+ userName.getText()+"'"+" and UPASS='"+passWord.getText()+"'";
        try {
            Con = DriverManager.getConnection("jdbc:derby://localhost:1527/StoreDB","User1","12345");
            St = Con.createStatement();
            Rs = St.executeQuery(query);
            if(Rs.next()){
                new HomeForm().setVisible(true);
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(this, "Wrong Username or Password");
            }
        } catch (Exception e) {
            //e.printStackTrace();
        }
        }
        
    }//GEN-LAST:event_LoginBtnMouseClicked

